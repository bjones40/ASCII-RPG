/*--------------------------------------------------------------------
 *
 * For stairs - 
 *      1: Initial spawn to dungeon
 *      2: Leads to 2
 *      3: Leads to 3
 *      4: Transitions to 3rd floor
 *
 *------------------------------------------------------------------
 */
void printMap()
{

    for(int rows=0; rows <= ROWS; rows++)
    {
        for (int columns=0; columns < COLUMNS; columns++)
        {   if( MAP[rows][columns] == '+' || MAP[rows][columns] == '-' || MAP[rows][columns] == '|' || MAP[rows][columns] == '1'  || MAP[rows][columns] == '2' || MAP[rows][columns] == '3' || MAP[rows][columns] == '4')
            {
                cout << ChangeToBlue();
            }
            else if( MAP[rows][columns] == '#' || MAP[rows][columns] == '*' || MAP[rows][columns] == 'X' )
            {
                cout << ChangeToWhite();
            }
            else if( MAP[rows][columns] == 'T' ) 
            {
                cout << ChangeToYellow();
            }    
            cout <<  MAP[rows][columns];
        }
        cout <<"\n";
    }
}

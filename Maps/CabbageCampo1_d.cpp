//--------------------------------------------------------------------
//
//  For stairs:
//      - Initial spawn is in bottom left corner
//      - Staircase in bottom right corner leads to 2nd floor
//
//--------------------------------------------------------------------
void printMap()
{

    for(int rows=0; rows <= ROWS; rows++)
    {
        for (int columns=0; columns < COLUMNS; columns++)
        {   if( MAP[rows][columns] == '+' || MAP[rows][columns] == '-' || MAP[rows][columns] == '|' || MAP[rows][columns] == 's')
            {
                cout << ChangeToBlue();
            }
            else if( MAP[rows][columns] == '#' || MAP[rows][columns] == '*' )
            {
                cout << ChangeToWhite();
            }

            cout <<  MAP[rows][columns];
        }
        cout <<"\n";
    }
}

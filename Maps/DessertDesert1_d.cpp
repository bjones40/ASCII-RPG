char MAP[][COLUMNS] =	
{
    "+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+",
    "|*************************************************************************|",
    "+*************************************************************************+",
    "|**********************##############################################*****|",
    "+**********************#                         #                  #*****+",
    "|**********************#                                       T T  #*****|",
    "+**********************#                         #             T T  #*****+",
    "|**********************#                         #                  #*****|",
    "+**********************#                         #                  #*****+",
    "|**********************#                         #                  #*****|",
    "+**********************#                         #                  #*****+",
    "|**********************#                         #   ################*****|",
    "+**********************#                                      #     #*****+",
    "|**********************#                                      #  T  #*****|",
    "+**********************#                                            #*****+",
    "|**********************#                                      #     #*****|",
    "+**********************#####################    #####################*****+",
    "|**********************#                                  #         #*****|",
    "+**********************#                                  #      s  #*****+",
    "|**********************#                                            #*****|",
    "+**********************##############################################*****+",
    "|*************************************************************************|",
    "+*************************************************************************+",
    "|*************************************************************************|",
    "+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+"
};
//--------------------------------------------------------------------
//
//  For stairs:
//      - 's' is for transition into Dessert Desert overworld.
//  
//  Other:
//      - Initial spawn will be somewhere in top left corner.
//      - 'T' is for treasure.
//--------------------------------------------------------------------
void printMap()
{

    for(int rows=0; rows <= ROWS; rows++)
    {
        for (int columns=0; columns < COLUMNS; columns++)
        {   if( MAP[rows][columns] == '+' || MAP[rows][columns] == '-' || MAP[rows][columns] == '|' || MAP[rows][columns] == 's' )
            {
                cout << ChangeToBlue();
            }
            else if( MAP[rows][columns] == '#' || MAP[rows][columns] == '*' || MAP[rows][columns] == 'X' )
            {
                cout << ChangeToWhite();
            }
            else if( MAP[rows][columns] == 'T' ) 
            {
                cout << ChangeToYellow();
            }    
            cout <<  MAP[rows][columns];
        }
        cout <<"\n";
    }
}
//---------------------------------------------------------------------


int main()
{

    system("clear");
    printMap();


    return 0;
}
